##3 Faça um Programa que pergunte quanto você ganha por hora e o
##número de horas trabalhadas no mês. Calcule e mostre o total do
##seu salário no referido mês.



recebePorHora    = float(input("Quanto você ganha por hora? "))
horasTrabalhadas = int(input("Quantas horas trabalhadas no mês? "))

salario = (recebePorHora * horasTrabalhadas)

print(salario)




    




